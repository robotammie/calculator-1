def add(num1, num2):
    """Adds num1 and num2"""

    result = num1 + num2

    return result


def subtract(num1, num2):
    """Subtracts num2 from num1"""

    return num1 - num2


def multiply(num1, num2):
    """multiplies num1 and num2"""

    return num1 * num2


def divide(num1, num2):
    """"divides num1 by num2, returns float"""

    num1 = float(num1)
    return num1 / num2


def square(num1):
    """provides square of num1"""

    return num1 * num1


def cube(num1):
    """provides cube of num1"""

    return num1 ** 3


